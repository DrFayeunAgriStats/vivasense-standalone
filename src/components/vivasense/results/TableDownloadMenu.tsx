import { Button } from "@/components/ui/button";
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import { Download, FileText, FileSpreadsheet, FileDown } from "lucide-react";
import * as XLSX from "xlsx";

interface Props {
  title: string;
  headers: string[];
  rows: unknown[][];
}

function safeName(title: string): string {
  return title.replace(/\s+/g, "_").replace(/[^a-zA-Z0-9_]/g, "").slice(0, 60);
}

function datestamp(): string {
  return new Date().toISOString().slice(0, 10);
}

function downloadWord(title: string, headers: string[], rows: unknown[][]) {
  const styles = `
    <style>
      body { font-family: 'Times New Roman', serif; font-size: 12pt; }
      table { border-collapse: collapse; width: 100%; margin-top: 12pt; }
      th, td { border: 1px solid #000; padding: 4pt 8pt; font-size: 11pt; }
      th { background-color: #f2f2f2; font-weight: bold; text-align: left; }
      td { text-align: right; }
      td:first-child { text-align: left; }
      caption { text-align: left; font-weight: bold; margin-bottom: 6pt; font-size: 11pt; }
      p.footer { font-size: 9pt; color: #666; margin-top: 20pt; text-align: center; }
    </style>`;

  let html = `<html><head><meta charset="utf-8">${styles}</head><body>`;
  html += `<table><caption>${title}</caption><thead><tr>`;
  headers.forEach((h) => { html += `<th>${h}</th>`; });
  html += `</tr></thead><tbody>`;
  rows.forEach((row) => {
    html += "<tr>";
    row.forEach((cell) => { html += `<td>${cell ?? "—"}</td>`; });
    html += "</tr>";
  });
  html += `</tbody></table>`;
  html += `<p class="footer">Generated by VivaSense — ${datestamp()}</p></body></html>`;

  const blob = new Blob([html], { type: "application/msword" });
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = `VivaSense_${safeName(title)}_${datestamp()}.doc`;
  a.click();
  URL.revokeObjectURL(a.href);
}

function downloadExcel(title: string, headers: string[], rows: unknown[][]) {
  const wb = XLSX.utils.book_new();
  const sheetData = [headers, ...rows];
  const ws = XLSX.utils.aoa_to_sheet(sheetData);
  XLSX.utils.book_append_sheet(wb, ws, safeName(title).slice(0, 31));
  XLSX.writeFile(wb, `VivaSense_${safeName(title)}_${datestamp()}.xlsx`);
}

function downloadCSV(title: string, headers: string[], rows: unknown[][]) {
  const escape = (v: unknown) => {
    const s = String(v ?? "");
    return s.includes(",") || s.includes('"') || s.includes("\n")
      ? `"${s.replace(/"/g, '""')}"`
      : s;
  };
  const lines = [headers.map(escape).join(","), ...rows.map((r) => r.map(escape).join(","))];
  const blob = new Blob([lines.join("\n")], { type: "text/csv" });
  const a = document.createElement("a");
  a.href = URL.createObjectURL(blob);
  a.download = `VivaSense_${safeName(title)}_${datestamp()}.csv`;
  a.click();
  URL.revokeObjectURL(a.href);
}

export function TableDownloadMenu({ title, headers, rows }: Props) {
  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" size="sm" className="text-xs gap-1.5">
          <Download className="w-3.5 h-3.5" />
          Download
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        <DropdownMenuItem onClick={() => downloadWord(title, headers, rows)}>
          <FileText className="w-4 h-4 mr-2" />
          Word (.doc)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={() => downloadExcel(title, headers, rows)}>
          <FileSpreadsheet className="w-4 h-4 mr-2" />
          Excel (.xlsx)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={() => downloadCSV(title, headers, rows)}>
          <FileDown className="w-4 h-4 mr-2" />
          CSV (.csv)
        </DropdownMenuItem>
      </DropdownMenuContent>
    </DropdownMenu>
  );
}

/** Helper: normalize unknown table data into { headers, rows } for the menu */
export function normalizeForDownload(data: unknown): { headers: string[]; rows: unknown[][] } | null {
  if (Array.isArray(data) && data.length > 0) {
    if (Array.isArray(data[0])) {
      const headers = (data[0] as unknown[]).map(String);
      return { headers, rows: data.slice(1) as unknown[][] };
    }
    if (typeof data[0] === "object" && data[0] !== null) {
      const headers = Object.keys(data[0] as Record<string, unknown>);
      const rows = (data as Record<string, unknown>[]).map((r) => headers.map((h) => r[h]));
      return { headers, rows };
    }
  }
  if (data && typeof data === "object" && !Array.isArray(data)) {
    const obj = data as Record<string, unknown>;
    const keys = Object.keys(obj);
    if (keys.length === 0) return null;
    const firstCol = obj[keys[0]];
    if (firstCol && typeof firstCol === "object" && !Array.isArray(firstCol)) {
      const rowKeys = Object.keys(firstCol as Record<string, unknown>);
      const rows = rowKeys.map((rk) => keys.map((h) => (obj[h] as Record<string, unknown>)?.[rk]));
      return { headers: keys, rows };
    }
    // Simple key-value
    return { headers: ["Parameter", "Value"], rows: keys.map((k) => [k, obj[k]]) };
  }
  return null;
}
