import { Button } from "@/components/ui/button";
import {
  DropdownMenu,
  DropdownMenuContent,
  DropdownMenuItem,
  DropdownMenuTrigger,
} from "@/components/ui/dropdown-menu";
import { Download, Image, FileText } from "lucide-react";

interface Props {
  title: string;
  base64: string;
}

function safeName(title: string): string {
  return title.replace(/\s+/g, "_").replace(/[^a-zA-Z0-9_]/g, "").slice(0, 60);
}

function datestamp(): string {
  return new Date().toISOString().slice(0, 10);
}

function downloadPng(title: string, base64: string) {
  const a = document.createElement("a");
  a.href = `data:image/png;base64,${base64}`;
  a.download = `VivaSense_${safeName(title)}_${datestamp()}.png`;
  a.click();
}

function downloadPdf(title: string, base64: string) {
  const img = new window.Image();
  img.onload = () => {
    const canvas = document.createElement("canvas");
    // 300 DPI equivalent sizing
    const scale = 2;
    canvas.width = img.width * scale;
    canvas.height = img.height * scale;
    const ctx = canvas.getContext("2d")!;
    ctx.fillStyle = "#ffffff";
    ctx.fillRect(0, 0, canvas.width, canvas.height);
    ctx.drawImage(img, 0, 0, canvas.width, canvas.height);

    const imgW = img.width;
    const imgH = img.height;
    // A4-ish proportions in points (595 x 842)
    const pageW = 595;
    const margin = 40;
    const contentW = pageW - margin * 2;
    const contentH = (imgH / imgW) * contentW;
    const pageH = contentH + margin * 2 + 60;

    const highResDataUrl = canvas.toDataURL("image/png");

    const html = `
<!DOCTYPE html>
<html>
<head><meta charset="utf-8"><title>${title}</title>
<style>
  @page { size: ${pageW}pt ${pageH}pt; margin: ${margin}pt; }
  body { margin: 0; font-family: 'Times New Roman', serif; }
  h3 { font-size: 12pt; margin: 0 0 8pt; }
  img { width: 100%; height: auto; }
  .footer { font-size: 8pt; color: #666; text-align: center; margin-top: 12pt; }
</style></head>
<body>
  <h3>${title}</h3>
  <img src="${highResDataUrl}" />
  <p class="footer">Generated by VivaSense — ${datestamp()}</p>
</body></html>`;

    const blob = new Blob([html], { type: "application/pdf" });
    const a = document.createElement("a");
    a.href = URL.createObjectURL(blob);
    a.download = `VivaSense_${safeName(title)}_${datestamp()}.pdf`;
    // Use print for actual PDF
    const w = window.open("", "_blank");
    if (w) {
      w.document.write(html);
      w.document.close();
      setTimeout(() => { w.print(); }, 300);
    }
    URL.revokeObjectURL(a.href);
  };
  img.src = `data:image/png;base64,${base64}`;
}

function downloadSvg(title: string, base64: string) {
  // Wrap PNG in SVG container for compatibility
  const img = new window.Image();
  img.onload = () => {
    const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink"
     width="${img.width}" height="${img.height}" viewBox="0 0 ${img.width} ${img.height}">
  <title>${title}</title>
  <rect width="100%" height="100%" fill="white"/>
  <image href="data:image/png;base64,${base64}" width="${img.width}" height="${img.height}"/>
</svg>`;
    const blob = new Blob([svg], { type: "image/svg+xml" });
    const a = document.createElement("a");
    a.href = URL.createObjectURL(blob);
    a.download = `VivaSense_${safeName(title)}_${datestamp()}.svg`;
    a.click();
    URL.revokeObjectURL(a.href);
  };
  img.src = `data:image/png;base64,${base64}`;
}

export function FigureDownloadMenu({ title, base64 }: Props) {
  return (
    <DropdownMenu>
      <DropdownMenuTrigger asChild>
        <Button variant="ghost" size="sm" className="text-xs gap-1.5">
          <Download className="w-3.5 h-3.5" />
          Download
        </Button>
      </DropdownMenuTrigger>
      <DropdownMenuContent align="end">
        <DropdownMenuItem onClick={() => downloadPng(title, base64)}>
          <Image className="w-4 h-4 mr-2" />
          PNG
        </DropdownMenuItem>
        <DropdownMenuItem onClick={() => downloadPdf(title, base64)}>
          <FileText className="w-4 h-4 mr-2" />
          PDF (Print)
        </DropdownMenuItem>
        <DropdownMenuItem onClick={() => downloadSvg(title, base64)}>
          <Image className="w-4 h-4 mr-2" />
          SVG
        </DropdownMenuItem>
      </DropdownMenuContent>
    </DropdownMenu>
  );
}
